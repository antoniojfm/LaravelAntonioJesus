{
    use Queueable, SerializesModels;

    public $name;

    /**
     * Create a new message instance.
     *
     * @return void
     */
    public function __construct($name)
    {
        $this->name = $name;
    }

    /**
     * Build the message.
     *
     * @return $this
     */
    public function build()
    {
        return $this->view('email')->from("antonio@test1.qastusoft.com.es", "Antonio FernÃ¡ndez")->subject('Aviso importante')->attachFromStorage('prueba.pdf');
    }
}
